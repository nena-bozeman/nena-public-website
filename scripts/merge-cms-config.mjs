#!/usr/bin/env node
/**
 * Merge config.base.yml with a backend overlay into public/admin/config.yml.
 *
 * Decap CMS loads a single YAML file and does not support includes, so we
 * concatenate at build time instead.
 *
 * Usage:
 *   node scripts/merge-cms-config.mjs main   # production (default)
 *   node scripts/merge-cms-config.mjs dev    # local decap-server
 */

import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const ADMIN = join(ROOT, 'public/admin');

const OVERLAYS = {
  main: 'config.main.yml',
  dev: 'config.dev.yml',
};

const target = process.argv[2] || 'main';
const overlayFile = OVERLAYS[target];

if (!overlayFile) {
  console.error(`Unknown target "${target}". Use: main | dev`);
  process.exit(1);
}

const base = readFileSync(join(ADMIN, 'config.base.yml'), 'utf8').trimEnd();
const overlay = readFileSync(join(ADMIN, overlayFile), 'utf8').trimEnd();
const header = `# Generated by scripts/merge-cms-config.mjs (${target}: ${overlayFile} + config.base.yml).\n# Do not edit directly — change the source files and re-run the merge script.\n\n`;

writeFileSync(join(ADMIN, 'config.yml'), `${header}${overlay}\n\n${base}\n`);
console.log(`Wrote public/admin/config.yml from ${overlayFile} + config.base.yml`);
