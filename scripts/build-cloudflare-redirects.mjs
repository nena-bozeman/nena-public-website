#!/usr/bin/env node
/**
 * Build Cloudflare redirect artifacts from data/legacy-inventory.json (run inventory:legacy first).
 *
 * Outputs:
 *   1) public/_redirects — Cloudflare Pages format: <source_path> <dest_path> 301
 *   2) data/cloudflare-bulk-redirects.csv — Bulk Redirects import (NO header row per Cloudflare docs)
 *
 * Environment:
 *   MIGRATION_TARGET_ORIGIN — required for bulk CSV, e.g. https://nena.pages.dev (no trailing slash)
 *   LEGACY_REDIRECT_SOURCE_HOST — default www.nenabozeman.org (scheme omitted in bulk source per CF CSV)
 *   LEGACY_ALSO_APEX=1 — duplicate each bulk row for host nenabozeman.org (no www)
 *
 * Usage: node scripts/build-cloudflare-redirects.mjs
 */

import { readFileSync, writeFileSync, existsSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..');
const INVENTORY = join(ROOT, 'data', 'legacy-inventory.json');
const OUT_PAGES = join(ROOT, 'public', '_redirects');
const OUT_BULK = join(ROOT, 'data', 'cloudflare-bulk-redirects.csv');
const OUT_BULK_NOTE = join(ROOT, 'data', 'cloudflare-bulk-redirects.env-note.txt');

const TARGET_ORIGIN = (process.env.MIGRATION_TARGET_ORIGIN || '').replace(/\/+$/, '');
const SOURCE_HOST_WWW = (process.env.LEGACY_REDIRECT_SOURCE_HOST || 'www.nenabozeman.org').replace(/^https?:\/\//, '').replace(/\/+$/, '');
const SOURCE_HOST_APEX = 'nenabozeman.org';
const ALSO_APEX = process.env.LEGACY_ALSO_APEX === '1' || process.env.LEGACY_ALSO_APEX === 'true';

const MIGRATED_RANK = { yes: 3, partial: 2, no: 1, unknown: 0, 'n/a': 0 };

function normPath(p) {
  if (!p || p === '/') return '/';
  const s = p.split('?')[0].split('#')[0];
  if (s === '/') return '/';
  return s.replace(/\/+$/, '') || '/';
}

function isConcreteSitePath(p) {
  if (!p || typeof p !== 'string') return false;
  const t = p.trim();
  if (!t.startsWith('/')) return false;
  if (t.includes('(') || t.includes(')')) return false;
  if (/\s/.test(t)) return false;
  return true;
}

function escapeCsvField(s) {
  const v = String(s);
  if (/[",\n\r]/.test(v)) return `"${v.replace(/"/g, '""')}"`;
  return v;
}

function main() {
  if (!existsSync(INVENTORY)) {
    console.error(`Missing ${INVENTORY}. Run: npm run inventory:legacy`);
    process.exit(1);
  }

  /** @type {Array<Record<string, string>>} */
  const rows = JSON.parse(readFileSync(INVENTORY, 'utf8'));

  /** @type {Map<string, { dest: string; rank: number; url: string }>} */
  const best = new Map();

  for (const row of rows) {
    if (row.kind !== 'page') continue;
    let sourcePath;
    try {
      sourcePath = normPath(new URL(row.url).pathname);
    } catch {
      continue;
    }
    const destRaw = (row.new_site_path || '').trim();
    if (!isConcreteSitePath(destRaw)) continue;
    const destPath = normPath(destRaw);
    if (sourcePath === destPath) continue;

    const rank = MIGRATED_RANK[row.migrated] ?? 0;
    const prev = best.get(sourcePath);
    if (!prev || rank > prev.rank) {
      best.set(sourcePath, { dest: destPath, rank, url: row.url });
    }
  }

  const rules = [...best.entries()]
    .map(([from, meta]) => ({ from, to: meta.dest, url: meta.url, rank: meta.rank }))
    .sort((a, b) => {
      const la = a.from.length;
      const lb = b.from.length;
      if (la !== lb) return lb - la;
      return a.from.localeCompare(b.from);
    });

  const linesPages = [
    '# Generated by scripts/build-cloudflare-redirects.mjs from data/legacy-inventory.json',
    '# Regenerate: npm run inventory:legacy && npm run redirects:legacy',
    '# Cloudflare Pages: https://developers.cloudflare.com/pages/configuration/redirects/',
    '',
  ];

  for (const r of rules) {
    linesPages.push(`${r.from} ${r.to} 301`);
  }
  linesPages.push('');
  writeFileSync(OUT_PAGES, linesPages.join('\n'), 'utf8');
  console.log(`Wrote ${rules.length} rules to ${OUT_PAGES}`);

  const bulkLines = [];
  if (!TARGET_ORIGIN) {
    console.warn(
      'Skipping data/cloudflare-bulk-redirects.csv (set MIGRATION_TARGET_ORIGIN to emit it). See data/cloudflare-bulk-redirects.env-note.txt',
    );
    writeFileSync(
      OUT_BULK_NOTE,
      [
        'Bulk Redirects CSV is not generated until you set a full origin for the NEW site.',
        '',
        'Example:',
        '  MIGRATION_TARGET_ORIGIN=https://nena.pages.dev npm run redirects:legacy',
        '',
        'Optional:',
        '  LEGACY_REDIRECT_SOURCE_HOST=www.nenabozeman.org   (default)',
        '  LEGACY_ALSO_APEX=1   (duplicate each row for nenabozeman.org without www)',
        '',
        'CSV rules (no header row):',
        'https://developers.cloudflare.com/rules/url-forwarding/bulk-redirects/reference/csv-file-format/',
      ].join('\n'),
      'utf8',
    );
  } else {
    const targetBase = TARGET_ORIGIN.startsWith('http') ? TARGET_ORIGIN : `https://${TARGET_ORIGIN}`;

    const emitRow = (sourceHost, fromPath, toPath) => {
      const src = `${sourceHost}${fromPath === '/' ? '' : fromPath}`;
      const tgt = `${targetBase}${toPath === '/' ? '/' : toPath}`;
      bulkLines.push([escapeCsvField(src), escapeCsvField(tgt), '301', '', '', '', ''].join(','));
    };

    for (const r of rules) {
      emitRow(SOURCE_HOST_WWW, r.from, r.to);
      if (ALSO_APEX) emitRow(SOURCE_HOST_APEX, r.from, r.to);
    }

    writeFileSync(OUT_BULK, bulkLines.join('\n') + '\n', 'utf8');
    console.log(`Wrote ${bulkLines.length} rows to ${OUT_BULK}`);
  }
}

main();
